// 2l. Convert 25°C to Fahrenheit
const celsius1 = 25;
const fahrenheit1 = (celsius1 * 9) / 5 + 32;
console.log(`${celsius1}°C is ${fahrenheit1}°F`);

// 2m. Convert 86°F to Celsius
const fahrenheit2 = 86;
const celsius2 = ((fahrenheit2 - 32) * 5) / 9;
console.log(`${fahrenheit2}°F is ${celsius2}°C`);

// 2n. Convert -5°C to Fahrenheit
const celsius3 = -5;
const fahrenheit3 = (celsius3 * 9) / 5 + 32;
console.log(`${celsius3}°C is ${fahrenheit3}°F`);
